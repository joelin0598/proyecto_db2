CREATE OR REPLACE PROCEDURE C##STAGE.PRC_SINCRONIZACION AS
BEGIN
  -- Paso 1: Borrar registros anteriores en la tabla Stage
  DELETE FROM C##STAGE.BD2_STG_DATOS;

  -- Paso 2: Insertar nueva información (simulada aquí desde una tabla temporal)
  INSERT INTO C##STAGE.BD2_STG_DATOS (
    ANIO, FECHA, HORA, RONDA, ESTADIO, CIUDAD, PAIS, 
    EQUIPO_LOCAL, GOL_LOCAL, EQUIPO_VISITA, GOL_VISITA, ASISTENCIA
  )
;

  -- Paso 3: Registrar que se hizo una sincronización
  INSERT INTO C##PRODUCCION.BD2_SEGUIMIENTO (
    DIMENSION,
    USUARIO,
    FECHA
  ) VALUES (
    'BD2_STG_DATOS',
    USER,
    SYSDATE
  );
END;

