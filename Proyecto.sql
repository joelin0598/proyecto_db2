CREATE OR REPLACE TRIGGER C##PRODUCCION.TRG_TABLA_SINC
AFTER INSERT OR UPDATE ON C##PRODUCCION.BD2_NO_HECHOS
FOR EACH ROW
BEGIN
  INSERT INTO C##PRODUCCION.BD2_SEGUIMIENTO (
    DIMENSION,
    USUARIO,
    FECHA
  ) VALUES (
    'BD2_NO_HECHOS',
    USER,
    SYSDATE
  );
END;
